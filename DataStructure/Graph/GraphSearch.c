// 图的遍历

/*---------------------深度优先遍历--------------------*/
/* 
   遍历思想：基本思想：首先从图中某个顶点v0出发，访问此顶点，
   然后依次从v相邻的顶点出发深度优先遍历，直至图中所有与v路径相通的顶点都被访问了；
   若此时尚有顶点未被访问，则从中选一个顶点作为起始点，重复上述过程，直到所有的顶点都被访问。
*/

/*---------------------广度优先遍历--------------------*/
/* 
   遍历思想：首先，从图的某个顶点v0出发，访问了v0之后，
   依次访问与v0相邻的未被访问的顶点，然后分别从这些顶点出发，
   广度优先遍历，直至所有的顶点都被访问完。
*/





